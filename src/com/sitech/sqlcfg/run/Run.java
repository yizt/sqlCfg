package com.sitech.sqlcfg.run;

/**
 * 脚本执行接口
 * 
 * @author yzt
 * 
 */
public interface Run {
	/**
	 * 执行脚本
	 * 
	 * @param funcId
	 *            功能标志
	 * @param dataTime
	 *            数据时间
	 * @param mode
	 *            模式 normal、redo执行，debug不执行
	 * @param businessParam
	 *            业务参数
	 * @return
	 * @throws Exception 
	 */
	public void run(String funcId, String dataTime, String mode,
			String businessParam) throws Exception;
}
